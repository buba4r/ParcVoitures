  private void jBajouterVoitureActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jBajouterVoitureActionPerformed
    // TODO add your handling code here:
    String marqueVoitureString = marqueVoiture.getText();
    String modeleVoitureString = ModeleVoiture.getText();
    int numeroSerieMoteur = Integer.parseInt(NumeroSerieVoiture.getText());
    int indexSelectedHiver = HiverPneu.getSelectedIndex();
    Boolean isHiver = new Boolean((HiverPneu.getItemAt(indexSelectedHiver)).toString());
    Double largeur = Double.parseDouble(LargeurPneu.getText());
    Double hauteur = Double.parseDouble(Hauteur.getText());
    String marquePneuString = marquePneu.getText();
    facade.creerVoiture(marqueVoitureString, modeleVoitureString, numeroSerieMoteur, largeur, hauteur, marquePneuString,isHiver);
    // facade.creerVoiture(marqueVoitureString, modeleVoitureString,
    // numeroSerieMoteur, largeur, hauteur, marquePneuString);
    facade.afficherListPneux();
    clearInput();
  }// GEN-LAST:event_jBajouterVoitureActionPerformed

